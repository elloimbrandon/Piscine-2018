char	*ft_strrev(char *str)
{
	int			i;
	int			x;
	char		temp;

	i = 0;
	x = 0;
	while (str[x] != '\0')
		x++;
	x--;
	while (i < x)
	{
		temp = str[x];
		str[x] = str[i];
		str[i] = temp;
		i++;
		x--;
	}
	return (str);
}
