void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	if (nb < 10)
	{
		nb = -nb;
	}
	if (nb > 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
		ft_putchar(nb + '0');
}
