int		ft_sqrt(int nb)
{
	int i;
	int result;

	i = 0;
	result = 1;
	if (nb == 0 || nb == 1)
		return (nb);
	while (result <= nb)
	{
		i++;
		result = i * i;
	}
	return (i - 1);
}
